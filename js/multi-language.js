// detect user language and set language to the languageMap Unsupported language will be set to English
function detectLanguage() {
    const language = navigator.language.split("-")[0];
    if (languageMap.has(language)) {
        languageindex = languageMap.get(language);
    } else {
        languageindex = 0;
    }
}

// use dropdown button to change language
function changeLanguage() {
    const language = languageMap.get(languageindex);
    document.getElementById("language").innerHTML = language;
    localStorage.setItem("language", languageindex);
}
